export const locale = ref<Locale>(resolveInitialLocale())

function resolveInitialLocale(): Locale {
  const stored = window.localStorage.getItem(STORAGE_KEY)
  if (stored === 'en' || stored === 'it') {
    return stored
  }

  const browser = window.navigator.language.toLowerCase()
  return browser.startsWith('it') ? 'it' : 'en'
}

export function setLocale(next: Locale) {
  locale.value = next
  window.localStorage.setItem(STORAGE_KEY, next)
}

function resolveMessage(key: string): string {
  const dictionary = messages[locale.value]
  return dictionary[key] ?? messages.en[key] ?? key
}

export function t(key: string, params: Record<string, string | number> = {}) {
  let message = resolveMessage(key)

  Object.entries(params).forEach(([token, value]) => {
    message = message.replaceAll(`{${token}}`, String(value))
  })

  return message
}
